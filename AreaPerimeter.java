public class AreaPerimeter {
	public static void main(String[] args) {

		int length, breadth, area, perimeter;
		length = 20;
		breadth = 30;
		area = length*breadth;
		perimeter = 2 * (length+breadth);
		System.out.println("Area: " + area);
		System.out.println("Perimeter: " + perimeter);



		
	}
}